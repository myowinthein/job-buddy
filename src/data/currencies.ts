export function getCurrencyLabel(c: Currency): string {
  return `${getFlag(c.countryCode)} ${c.code} — ${c.name}`;
}

export function findCurrency(code: string): Currency | undefined {
  return CURRENCIES.find((c) => c.code === code.toUpperCase());
}

