export function findLanguage(codeOrName: string): Language | undefined {
  const lower = codeOrName.toLowerCase();
  return (
    LANGUAGES.find((l) => l.code === lower) ??
    LANGUAGES.find((l) => l.name.toLowerCase() === lower)
  );
}
